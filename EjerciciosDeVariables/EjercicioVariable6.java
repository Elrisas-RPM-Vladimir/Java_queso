package EjerciciosDeVariables;

public class EjercicioVariable6 {
    public static void main(String[] args) {
        // Declarar variables de tipo char
        char letra1 = 'H';
        char letra2 = 'o';
        char letra3 = 'l';
        char letra4 = 'a';
        char letra5 = '!';

        // Concatenar las variables de tipo char en una variable String
        String resultadoConcatenacion = "" + letra1 + letra2 + letra3 + letra4 + letra5;
 
        // Mostrar la cadena de caracteres resultante
        System.out.println(resultadoConcatenacion);
    }
}
